"""Machine-readable lifecycle keyword and effect registry.

This module is deliberately descriptive: pipeline parsing, validation, and
execution do not import it.  Documentation and downstream presentation layers
can consume :func:`get_keyword_registry` without making this registry a second
runtime dispatcher.

The first registry slice focuses on the terms that are easy to confuse while
native DAG tuning and conformal calibration are introduced.  ``planned`` and
``partial`` are contract states, not promises that a surface already executes.

``engine_support`` keys name the actor whose capability is described:
``legacy``, ``dag-ml`` and ``dual`` are execution backends, while ``optuna``
and ``n4m`` are optimizer engines.  An entry may list both namespaces when a
feature crosses the execution/optimization boundary.
"""

from __future__ import annotations

import copy
import json
from typing import Any, Final, Literal, TypedDict

KeywordStatus = Literal["supported", "partial", "planned"]
KeywordSurface = Literal[
    "run_argument",
    "pipeline_step",
    "nested_key",
    "enum_value",
    "calibrate_argument",
    "predict_argument",
    "predict_calibrated_argument",
    "robustness_argument",
]
CalibrationInvalidation = Literal[
    "always",
    "if_predictor_changes",
    "replaces_existing",
    "extends_existing",
    "mode_dependent",
    "not_applicable",
]
EngineSupport = Literal["supported", "partial", "planned", "unsupported", "legacy_fallback", "not_applicable"]


class AliasSpec(TypedDict):
    """One accepted legacy spelling or value."""

    kind: Literal["token", "value"]
    name: str
    canonical: str
    mode: Literal["read_only"]


class UIHint(TypedDict):
    """Presentation hint shared by documentation and future Studio forms."""

    label: str
    group: Literal["execution", "tuning", "training", "retraining", "calibration", "prediction", "robustness"]
    control: Literal["object", "array", "select", "number", "dataset", "path", "text"]
    order: int


class KeywordEntry(TypedDict):
    """Stable JSON shape for one lifecycle term."""

    id: str
    token: str
    path: str
    surface: KeywordSurface
    scope: str
    canonical_term: str
    status: KeywordStatus
    value_schema: dict[str, Any]
    aliases: list[AliasSpec]
    lifecycle_stage: str
    reads: list[str]
    changes: list[str]
    invalidates_calibration: CalibrationInvalidation
    engine_support: dict[str, EngineSupport]
    summary: str
    docs_anchor: str
    ui: UIHint


class KeywordRegistry(TypedDict):
    """Top-level deterministic registry document."""

    schema_id: str
    schema_version: int
    registry_version: str
    scope: str
    entries: list[KeywordEntry]


